// API fetcher
export const apiFetcher =
  (route, method, field, hasHeaders = true, hasFile = false) =>
  async (body = undefined, query = undefined) => {
    // Default options
    const options = {
      credentials: "include",
    };

    // Headers
    if (hasHeaders)
      options.headers = {
        "Content-Type": "application/json",
      };

    // Method
    options.method = method;

    // Get body
    if (body) options.body = hasFile ? body : JSON.stringify(body);

    // Send request
    const response = await fetch(
      `${API_URL}/${route}${query ? `${query}` : ""}`,
      options
    );

    // Get data
    const data = await response.json();
    if (data.statusCode === 200) {
      // Return data
      if (data[field]) return data[field];
    } else {
      // Create error
      const error = new Error(data.data.msg);
      // Set status
      error.status = data.statusCode;
      // Throw error
      throw error;
    }
  };

